 Techopedia explains Front-End Developer
 A front-end developer is a programmer that codes the front end of a website. Typically, the front-end developer's job is to convert website design files into raw HTML, 
 JavaScript (JS) and/or CSS code. This includes the basic website design/layout, images, content, buttons, navigation and internal links. The end result is code that serves as the website's front-end structure, which is used by a back-end developer to add business logics and connect databases and processes, among other processes.
